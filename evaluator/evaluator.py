def evaluate_correctness(prediction_file: str, 
                        problem_file: str, 
                        timeout: float = 5.0 ):
    # add prediction into problems
    problems = read_problems(problem_file)
    for sample in read_jsonl(prediction_file):
        task_id = sample['task_id']
        problems[task_id]['completion'] = sample['completion']
        if "gpt" in prediction_file:
            problems[task_id]['overhead'] = 0
        else:
            problems[task_id]['overhead'] = sample['overhead']
    # collect into batch
    task_groups = defaultdict(list)
    for task_id, problem in problems.items():
        task_groups[problem['task_type']].append(problem)
    # evaluate task-by-task
    task_results = defaultdict(dict)
    for task_type, task_group in task_groups.items():
        print(task_type, len(task_group))
        if task_type not in TASKTYPE_EVALUATOR_DICT:
            logging.warn(f'[!] No evaluator for task {task_type}, use default evaluator.')
            evaluator = DefaultEvaluator()
        else:
            evaluator = eval(TASKTYPE_EVALUATOR_DICT[task_type])()
        metrics = defaultdict(float)
        for problem in task_group:
            res = evaluator.evaluate(problem)
            problem['results'] = res
            metrics = {metric:metrics[metric]+res[metric] for metric in res}
        metrics = {metric:metrics[metric]/len(task_group) for metric in metrics}
        task_results[task_type] = metrics
    return task_results
    