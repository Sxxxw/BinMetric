class Evaluator:
    def evaluate(self, problem: dict) -> dict:
        '''
        @para problem: dict, contain all information needed to evaluate a solution of the problem
            field: 
                task_id: int, unique id of the problem
                task_type: str, type of the problem
                input: str, input of the problem
                output: str, output of the problem, i.e., label
                default_instruction: str, default instruction of the problem
                completion: str, completion of the problem, generated by model
                language[optional]: str, language of the problem, [C,C++,Python,JavaScript,ASM_x86,ASM_x64,ASM_ARM,ASM_MIPS]
                test_cases[optional]: list[list[str, str]], inputs of the program, in each item, item[0] is input, item[1] is expected output
        @return: dict, evaluation metrics, e.g., {"metric_a": 0.5, "metric_b": 0.6}
        '''
        raise NotImplementedError

