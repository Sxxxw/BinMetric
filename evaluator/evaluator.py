def parse_bleu_score(bleu_output):
    pattern = r"BLEU = ([\d.]+) ([\d.]+)/([\d.]+)/([\d.]+)/([\d.]+) \(BP = ([\d.]+) ratio = ([\d.]+) hyp_len = (\d+) ref_len = (\d+)\)"
    match = re.match(pattern, bleu_output)
    if match:
        bleu_score = {
            'BLEU': float(match.group(1)),
            'BLEU_1': float(match.group(2)),
            'BLEU_2': float(match.group(3)),
            'BLEU_3': float(match.group(4)),
            'BLEU_4': float(match.group(5)),
        }
        return bleu_score
    else:
        return None

class SemanticComprehensionEvaluator(Evaluator):
    def __init__(self) -> None:
        super().__init__()
        #self.scorer = rouge_scorer.RougeScorer(['rouge1','rouge2','rougeL'], use_stemmer=True)
        self.smooth_bleu_metric = []
        self.meteor_metric = evaluate.load('meteor')
        self.rouge_metric = evaluate.load('rouge')

    def evaluate(self, problem) -> dict:
        input_code = problem.get("input")
        prediction = problem.get('completion')
        reference = problem.get('output')
        bleu = sacrebleu.corpus_bleu([prediction], [[reference]], smooth_method='exp')
        meteor_score = self.meteor_metric.compute(predictions=[prediction], references=[reference])
        rouge_score = self.rouge_metric.compute(predictions=[prediction], references=[reference])

        parsed_bleu = parse_bleu_score(str(bleu))
        return {**parsed_bleu, **meteor_score, **rouge_score, "overhead": problem.get('overhead')}


